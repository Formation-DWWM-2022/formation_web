    public function findAllByCategoryParent(Category $category)
    {
        return $this->createQueryBuilder('m')
            ->where('m.category = :category')
            ->andWhere('m.message is null')
            ->setParameter('category', $category->getId())
            ->orderBy('m.dateCreated', 'DESC')
            ->getQuery()
            ->getResult();
    }

    public function findAllByCategoryParentQueryBuilder(Category $category): QueryBuilder
    {
        return $this->createQueryBuilder('m')
            ->where('m.category = :category')
            ->andWhere('m.message is null')
            ->setParameter('category', $category->getId())
            ->orderBy('m.dateCreated', 'DESC');
    }
